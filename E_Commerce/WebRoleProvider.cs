        public override string[] GetRolesForUser(string username)
        {
            using (var context = new E_CommerceDBEntities())
            {
                var result = (from user in context.tblAdminRegister
                              join role in context.tblRoles on user.AdminID equals role.AdminID
                              where user.UserName == username
                              select role.RolesName).ToArray();
                return result;
            }
        }

